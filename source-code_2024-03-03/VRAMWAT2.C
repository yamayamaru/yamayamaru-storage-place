void pc9821_on(void)
{
#if _HSYNC_
    // HSYNC -> 31.47 KHz 
    if( (inp(0x09a8) & 0x01) == 0){
        outp(0x09a8,0x01);
        Hmode = 1;
    }
#endif

    // 256 color mode
    outp(0x6a,0x07);
    outp(0x6a,0x21);
    outp(0x6a,0x06);

    // Packed Pixel
    *((char *)0xe0100) = 0x00;

    // 16[MB] System Area -> VRAM ON
    *((char *)0xe0102) = 0x01;

    // Graphics Start
    outp(0xa2,0x0d);
}

void pc9821_off()
{
    // 16[MB] System Area -> VRAM OFF
    *((char *)0xe0102) = 0x00;

    // 16 Color (standard) mode
    outp(0x6a,0x07);
    outp(0x6a,0x20);
    outp(0x6a,0x06);

#if _HSYNC_
    // HSYNC -> 24.8? KHz
    if(Hmode==1)
        outp(0x09a8,0x00);
#endif

    // TEXT Start
    outp(0x62,0x0d);

}

void dpmi_vram_off(DPMI_MEM *dm)
{
    union REGS r;
    X tmp;

    if(dm->selecter != 0xFFFFFFFF){
        r.x.eax = 0x0001;        // DPMI Function
        r.x.ebx = dm->selecter;
        int386(0x31, &r, &r);
        if( r.x.cflag ){
            // Failed
            puts("ディスクリプタを開放できませんでした。");
        } else {
            dm->selecter = 0xFFFFFFFF;
        }
    }

    if(dm->l_address != 0xFFFFFFFF){
        r.x.eax = 0x0801;        // DPMI Function
        tmp.x = dm->l_address;    // BX:CX = メモリの物理アドレス
        r.x.ebx = tmp.w.h;
        r.x.ecx = tmp.w.l;

        int386(0x31, &r, &r);
        if( r.x.cflag ){
            // Failed
            puts("物理アドレスのマッピングの解除ができませんでした。");
        } else {
            dm->l_address = 0xFFFFFFFF;
        }
    }
}

int dpmi_vram_on(DPMI_MEM *dm)
{
    union REGS r;
    X tmp;

    // ・ディスクリプタの割り当て
    r.x.eax = 0x0000;        // DPMI Function
    r.x.ecx = 1;            // アロケートするディスクリプタの数
    int386(0x31, &r, &r);
    if( r.x.cflag ){
        // Failed
        puts("ディスクリプタをアロケートできませんでした。");
        return(0);
    }
    // アロケートされたセレクタ
    dm->selecter = r.x.eax;


    // ・物理アドレスのマッピング
    r.x.eax = 0x0800;        // DPMI Function
    tmp.x = dm->p_address;    // BX:CX = メモリの物理アドレス
    r.x.ebx = tmp.w.h;
    r.x.ecx = tmp.w.l;
    tmp.x = dm->p_size;        // SI:DI = マップする領域のバイト数
    r.x.esi = tmp.w.h;
    r.x.edi = tmp.w.l;
    int386(0x31, &r, &r);
    if( r.x.cflag ){
        // Failed
        puts("物理アドレスをマッピングできませんでした。");
        return(0);
    }
    // physical address mapping -> Linear Address
    tmp.w.h = r.x.ebx;
    tmp.w.l = r.x.ecx;
    dm->l_address = tmp.x;


    // ・セグメントのベースアドレスを設定
    r.x.eax = 0x0007;        // DPMI Function
    r.x.ebx = dm->selecter;        // セレクタ
    // CX:DX = セグメントに対する新しい32ビットリニアベースアドレス
    r.x.ecx = tmp.w.h;
    r.x.edx = tmp.w.l;
    int386(0x31, &r, &r);
    if( r.x.cflag ){
        // Failed
        puts("ベースアドレスを設定できませんでした。");
        return(0);
    }

    // ・セグメントのリミットを設定
    r.x.eax = 0x0008;        // DPMI Function
    r.x.ebx = dm->selecter;        // セレクタ
    tmp.x = dm->p_size;        // リミットサイズ
    r.x.ecx = tmp.w.h;
    r.x.edx = tmp.w.l;
    int386(0x31, &r, &r);
    if( r.x.cflag ){
        // Failed
        puts("リミットを設定できませんでした。");
        return(0);
    }

    return(1);
}

