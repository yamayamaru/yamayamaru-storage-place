void setimage01(void) {
    int size;
    int i, j;
    unsigned int color, l, r, g, b;

    image01 = (byte *)malloc(RASPBERRY_WIDTH * RASPBERRY_HEIGHT);

    // 24bitRGB画像から256colorのイメージを作成
    for (i = 0; i < RASPBERRY_WIDTH * RASPBERRY_HEIGHT; i++) {
        if ((bitmap_mask01[i / 8] >> (7 - i % 8)) & 0x1) {
            r = bitmap01[i * 3 + 0];
            g = bitmap01[i * 3 + 1];
            b = bitmap01[i * 3 + 2];
            l = 0;
        } else {
            r = 0;
            g = 0;
            b = 0;
            l = 0;
        }
        if (r > 128 || g > 128 || b > 128)  l = 1;

        color = (l ? 8 : 0) | ((r != 0) ? 4 : 0) | ((g != 0) ? 2 : 0) | ((b != 0) ? 1 : 0);
        image01[i] = color;
    }



    // マスクの作成
    image_mask01 = (byte *)malloc(RASPBERRY_WIDTH * RASPBERRY_HEIGHT);
    for (i = 0; i < RASPBERRY_WIDTH * RASPBERRY_HEIGHT; i++) {
        if ((bitmap_mask01[i / 8] << (i % 8)) & 0x80) {
            image_mask01[i] = 0xff;
        } else {
            image_mask01[i] = 0;
        }
    }


    // 24bitRGB画像から256colorのイメージを作成(背景)
    image02 = (byte *)malloc(BACK01_WIDTH * BACK01_HEIGHT);
    for (i = 0; i < BACK01_WIDTH * BACK01_HEIGHT; i++) {
        r = bitmap02[i * 3 + 0];
        g = bitmap02[i * 3 + 1];
        b = bitmap02[i * 3 + 2];
        l = 0;
        if (r > 128 || g > 128 || b > 128)  l = 1;

        color = (l ? 8 : 0) | ((r != 0) ? 4 : 0) | ((g != 0) ? 2 : 0) | ((b != 0) ? 1 : 0);
        image02[i] = color;
    }

}




