void screen_update(void) {
    int num, numall, pos1;
    numall = 0;
    while (numall < SCREEN_WIDTH*SCREEN_HEIGHT) {
        pos1 = numall;
        num = memcpy((void *)(VGA + pos1), (void *)(double_buffer + pos1), (size_t)(SCREEN_WIDTH * SCREEN_HEIGHT - pos1));
        numall += num;
    }
}

