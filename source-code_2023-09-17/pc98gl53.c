#define RASPBERRY_WIDTH   32
#define RASPBBRRY_HEIGHT  32
#define BACK01_WIDTH      32
#define BACK01_HEIGHT     32

void drawimage01(void) {
    int size;
    int i, j;
    unsigned int color, l, r, g, b;

    fill_blue((char)0);
    fill_red((char)0);
    fill_green((char)0);
    fill_int((char)0);

    for (i = 0; i < RASPBERRY_WIDTH * RASPBBRRY_HEIGHT; i++) {
        if ((bitmap_mask01[i / 8] >> (7 - i % 8)) & 0x1) {
            r = bitmap01[i * 3 + 0];
            g = bitmap01[i * 3 + 1];
            b = bitmap01[i * 3 + 2];
            l = 0;
        } else {
            r = 0;
            g = 0;
            b = 0;
            l = 0;
        }
        if (r > 128 || g > 128 || b > 128)  l = 1;

        color = (l ? 8 : 0) | ((r != 0) ? 4 : 0) | ((g != 0) ? 2 : 0) | ((b != 0) ? 1 : 0);
        pset(i % RASPBERRY_WIDTH, i / RASPBERRY_WIDTH, color);
    }

    for (i = 0; i < RASPBERRY_WIDTH * RASPBBRRY_HEIGHT; i++) {
        if ((bitmap_mask01[i / 8] >> (7 - i % 8)) & 0x1) color = 0;
        else                                             color = 15;
        pset(RASPBERRY_WIDTH + i % RASPBERRY_WIDTH, i / RASPBERRY_WIDTH, color);
    }

    size = get_image_size(RASPBERRY_WIDTH, RASPBBRRY_HEIGHT);
    image01 = (void *)malloc(size);
    get_image(0, 0, RASPBERRY_WIDTH, RASPBBRRY_HEIGHT, image01);

    image_mask01 = (void *)malloc(size);
    get_image(RASPBERRY_WIDTH, 0, RASPBERRY_WIDTH, RASPBBRRY_HEIGHT, image_mask01);


    for (i = 0; i < BACK01_WIDTH * BACK01_HEIGHT; i++) {
        r = bitmap02[i * 3 + 0];
        g = bitmap02[i * 3 + 1];
        b = bitmap02[i * 3 + 2];
        l = 0;
        if (r > 128 || g > 128 || b > 128)  l = 1;

        color = (l ? 8 : 0) | ((r != 0) ? 4 : 0) | ((g != 0) ? 2 : 0) | ((b != 0) ? 1 : 0);
        pset(BACK01_WIDTH * 2 + i % BACK01_WIDTH, i / BACK01_WIDTH, color);
    }
    size = get_image_size(32, 32);
    image02 = (void *)malloc(size);
    get_image(RASPBERRY_WIDTH * 2, 0, 32, 32, image02);

}




int apage01 = 0;
int vpage01 = 1;
int rnd(int max);

struct {
    int    x;
    int    y;
    int    dx;
    int    dy;
    int    signx;
    int    signy;
    int    c;
} raspberry_data01[RASPBERRY_N];



void setup1(void){
    int i;
    for (i = 0; i < RASPBERRY_N; i++) {
        raspberry_data01[i].x = rnd(WIDTH);
        raspberry_data01[i].y = rnd(HEIGHT);
        raspberry_data01[i].dx = rnd(RDX) + 1;
        raspberry_data01[i].dy = rnd(RDY) + 1;
        raspberry_data01[i].signx = rnd(2) ? 1 : -1;
        raspberry_data01[i].signy = rnd(2) ? 1 : -1;
    }
}

void random_raspberry(void) {

    int temp1, temp2;
    int i, j;

    if (apage01 == 0) apage01 = 1;
    else              apage01 = 0;
    if (apage01 == 0) vpage01 = 0;
    else              vpage01 = 1;

    setactivepage(apage01);

    fill_blue(0x00);
    fill_red(0x00);
    fill_green(0x00);
    fill_int(0x00);

    for (j = 0; j < PC98_GLIB_HEIGHT / 32; j++) {
        for (i = 0; i < PC98_GLIB_WIDTH / 32; i++) {
            put_image(i * 32, j * 32, image02);
        }
    }

    for (i = 0; i < RASPBERRY_N; i++ ) {
        temp1 = raspberry_data01[i].dx * raspberry_data01[i].signx;
        temp2 = raspberry_data01[i].x + temp1;
        if (temp2 > WIDTH) {
            raspberry_data01[i].signx = -1;
            raspberry_data01[i].dx = rnd(RDX) + 1;
            raspberry_data01[i].x = WIDTH + raspberry_data01[i].signx * raspberry_data01[i].dx;
        } else if (temp2 < 0 ) {
            raspberry_data01[i].signx = 1;
            raspberry_data01[i].dx = rnd(RDX) + 1;
            raspberry_data01[i].x = 0 + raspberry_data01[i].signx * raspberry_data01[i].dx;
        } else {
            raspberry_data01[i].x = raspberry_data01[i].x + temp1;
        } 
        temp1 = raspberry_data01[i].dy * raspberry_data01[i].signy;
        temp2 = raspberry_data01[i].y + temp1;
        if (temp2 > HEIGHT) {
            raspberry_data01[i].signy = -1;
            raspberry_data01[i].dy = rnd(RDY) + 1;
            raspberry_data01[i].y = HEIGHT + raspberry_data01[i].signy * raspberry_data01[i].dy;
        } else if (temp2 < 0 ) {
            raspberry_data01[i].signy = 1;
            raspberry_data01[i].dy = rnd(RDY) + 1;
            raspberry_data01[i].y = 0 + raspberry_data01[i].signy * raspberry_data01[i].dy;
        } else {
            raspberry_data01[i].y = raspberry_data01[i].y + temp1;
        }
        if (raspberry_data01[i].y >= 0 && raspberry_data01[i].y < HEIGHT) {
            put_image_and(raspberry_data01[i].x, raspberry_data01[i].y, image_mask01);
            put_image_or(raspberry_data01[i].x, raspberry_data01[i].y, image01);
            /* put_image(raspberry_data01[i].x, raspberry_data01[i].y, image01); */
        }
    }

    setvisualpage(vpage01);

/*    delay(1);     */
}

