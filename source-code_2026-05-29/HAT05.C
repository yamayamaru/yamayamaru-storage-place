void put_vram_char(int x, int y, int ch, int attri) {
    unsigned int p01;
    unsigned int d;
    d = 2 * (x + y * 80);
    p01 = TEXT_VRAM;
    *(cvram01 + p01 + d) = (char)ch;            // ö╝ŖpāLāāāēāNā^é╠é▌;
    *(cvram01 + p01 + d + 1) = 0;
    p01 = TEXT_VRAM_ATTRI;
    *(cvram01 + p01 + d) = (char)attri;
    *(cvram01 + p01 + d + 1) = (char)0;
}

