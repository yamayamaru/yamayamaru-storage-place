char	mwork[ MosWorkSize ] ;

void main()
{
		int		ch, cl, dx, bx, bc ;

		EGB_init( work, EgbWorkSize ) ;
		EGB_resolution( work, 0, 1 ) ;
		EGB_resolution( work, 1, 1 ) ;
		EGB_displayPage( work, 0, 1) ;
		MOS_start( mwork, MosWorkSize ) ;
		MOS_resolution( 0, 1 ) ;
		MOS_disp( 1 ) ;

		MOS_type( 1, 16, 16, (char *)g_32_32 ) ;

		ch = 0 ;
		cl = 0 ;
		do
		{
			if ( ch == 1 )
			{
				switch ( cl++ )
				{
					case 0 :
						MOS_tilePattern( 1,  8, (char *)tile_pat1 ) ;
						break ;
					case 1 :
						MOS_tilePattern( 2, 16, (char *)tile_pat2 ) ;
						break ;
					case 2 :
						MOS_tilePattern( 3, 24, (char *)tile_pat3 ) ;
						break ;
					case 3 :
						MOS_tilePattern( 4, 32, (char *)tile_pat4 ) ;
						break ;
				} ;
				cl &= 0x3 ;
			}
			if( ch == 1 )
				do{
					MOS_rdopen( 0, &ch, &bc, &dx, &bx ) ;
				}while( !bc ) ;
			MOS_rdpos( &ch, &dx, &bx ) ;
		} while (ch != 3) ;
 		MOS_end() ;
}
